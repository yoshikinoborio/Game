#include "stdafx.h"
#include "SceneManager.h"
//-----------------------------------------------------------------------------
// Name: 初期化。
//-----------------------------------------------------------------------------
void Init()
{
	g_effectManager = new EffectManager;
	g_sceneManager = new SceneManager;
	g_sceneManager->Initialize();
}

//-----------------------------------------------------------------------------
// Name: 描画処理。
//-----------------------------------------------------------------------------
void Render()
{
	g_sceneManager->Draw();
}

/*!-----------------------------------------------------------------------------
*@brief	更新処理。
-----------------------------------------------------------------------------*/
void Update()
{
	g_sceneManager->Update();
}

//-----------------------------------------------------------------------------
//ゲームが終了するときに呼ばれる処理。
//-----------------------------------------------------------------------------
void Terminate()
{
	g_sceneManager->Terminate();
}
